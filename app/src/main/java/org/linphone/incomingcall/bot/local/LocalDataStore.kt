    fun listDatasets(): List<String> {
        val datasets = linkedSetOf<String>()
        val dayPattern = Regex("^([A-Za-z]+)-day-(\\d{4}-\\d{2}-\\d{2})-(\\d+)m$")
        val monthPattern = Regex("^([A-Za-z]+)-month-(\\d{4}-\\d{2})-(\\d+)m$")
        val dateRangePattern = Regex("^([A-Za-z]+)-date-(\\d{4}-\\d{2}-\\d{2})_(\\d{4}-\\d{2}-\\d{2})-(\\d+)m$")
        val cachePattern = Regex("^candles_(\\d+)_(\\d{4}-\\d{2}-\\d{2})$")

        fun parseName(rawName: String) {
            val name = rawName.removeSuffix(".json")
            when {
                dayPattern.matches(name) -> {
                    val m = dayPattern.find(name) ?: return
                    datasets += "${m.groupValues[1]}-day-${m.groupValues[2]}"
                }
                monthPattern.matches(name) -> {
                    val m = monthPattern.find(name) ?: return
                    datasets += "${m.groupValues[1]}-month-${m.groupValues[2]}"
                }
                dateRangePattern.matches(name) -> {
                    val m = dateRangePattern.find(name) ?: return
                    datasets += "${m.groupValues[1]}-date-${m.groupValues[2]}_${m.groupValues[3]}"
                }
                cachePattern.matches(name) -> {
                    val m = cachePattern.find(name) ?: return
                    datasets += "C-day-${m.groupValues[2]}"
                }
            }
        }

        mockDir.listFiles()?.forEach { parseName(it.name) }
        cacheDir.listFiles()?.forEach { parseName(it.name) }
        return datasets.toList().sortedDescending()
    }

    fun loadDatasetCandles(datasetId: String, tf: String = "1"): List<LocalCandle> {
        val dayPattern = Regex("^([A-Za-z]+)-day-(\\d{4}-\\d{2}-\\d{2})$")
        val monthPattern = Regex("^([A-Za-z]+)-month-(\\d{4}-\\d{2})$")
        val rangePattern = Regex("^([A-Za-z]+)-date-(\\d{4}-\\d{2}-\\d{2})_(\\d{4}-\\d{2}-\\d{2})$")

        val files = mutableListOf<File>()

        fun appendDay(date: String) {
            val mockFile = File(mockDir, "M-day-$date-${tf}m.json")
            if (mockFile.exists()) files += mockFile
            val cacheFile = File(cacheDir, "candles_${tf}_${date}.json")
            if (cacheFile.exists()) files += cacheFile
            // Tolerate filename variants generated by older app versions.
            mockDir.listFiles()?.forEach { f ->
                val n = f.name
                if (n == "M-day-$date-${tf}m.json") files += f
                if (n == "SH-day-$date-${tf}m.json") files += f
            }
            cacheDir.listFiles()?.forEach { f ->
                val n = f.name
                if (n == "candles_${tf}_${date}.json") files += f
            }
        }

        when {
            dayPattern.matches(datasetId) -> {
                val m = dayPattern.find(datasetId) ?: return emptyList()
                appendDay(m.groupValues[2])
            }
            monthPattern.matches(datasetId) -> {
                val m = monthPattern.find(datasetId) ?: return emptyList()
                val ym = YearMonth.parse(m.groupValues[2])
                for (d in 1..ym.lengthOfMonth()) {
                    appendDay(ym.atDay(d).format(DateTimeFormatter.ISO_DATE))
                }
            }
            rangePattern.matches(datasetId) -> {
                val m = rangePattern.find(datasetId) ?: return emptyList()
                var cursor = LocalDate.parse(m.groupValues[2])
                val end = LocalDate.parse(m.groupValues[3])
                while (!cursor.isAfter(end)) {
                    appendDay(cursor.format(DateTimeFormatter.ISO_DATE))
                    cursor = cursor.plusDays(1)
                }
            }
            else -> {
                Log.w(tag, "loadDatasetCandles unknown dataset format datasetId=$datasetId")
                return emptyList()
            }
        }

        Log.i(
            tag,
            "loadDatasetCandles dataset=$datasetId tf=$tf candidates=${files.distinctBy { it.absolutePath }.size}"
        )
        files.distinctBy { it.absolutePath }.forEach { f ->
            Log.d(tag, "candidate path=${f.absolutePath} exists=${f.exists()} size=${f.length()}")
        }

        val out = mutableListOf<LocalCandle>()
        files.distinctBy { it.absolutePath }.forEach { file ->
            try {
                val root = JsonParser.parseString(file.readText())
                if (!root.isJsonArray) return@forEach
                val arr = root.asJsonArray
                for (i in 0 until arr.size()) {
                    val o = arr[i].asJsonObject
                    val time = o.get("time")?.asLong ?: continue
                    val open = o.get("open")?.asDouble ?: continue
                    val high = o.get("high")?.asDouble ?: continue
                    val low = o.get("low")?.asDouble ?: continue
                    val close = o.get("close")?.asDouble ?: continue
                    out += LocalCandle(time, open, high, low, close)
                }
            } catch (_: Exception) {
                // Ignore corrupt files and continue with the rest.
            }
        }
        val result = out.distinctBy { it.time }.sortedBy { it.time }
        Log.i(tag, "loadDatasetCandles loaded dataset=$datasetId candles=${result.size}")
        if (dayPattern.matches(datasetId)) {
            val expected = expectedCountForTf(tf)
            if (expected != null && result.size > expected * 2) {
                Log.w(
                    tag,
                    "loadDatasetCandles count=${result.size} expected~$expected for 1 day tf=${tf}m — file likely downloaded with old resolution=1 (sub-minute). Delete mock and re-download."
                )
            }
        }
        return result
    }

