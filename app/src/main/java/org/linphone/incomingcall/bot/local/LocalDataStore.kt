    suspend fun mockDownloadDay(gregorianDate: String, tf: String = "1"): JsonObject {
        val (from, to) = LocalBotRuntime.gregorianDayRange(gregorianDate)
        val tfs = if (tf == "1") listOf("1", "5", "15", "30", "60") else listOf(tf)
        var total = 0
        tfs.forEach { timeframe ->
            val file = File(mockDir, "M-day-$gregorianDate-${timeframe}m.json")
            val before = readCandlesIfExists(file)
            val candles = HivaRoomClient.getBars(timeframe, from, to)
            file.writeText(candlesToJson(candles))
            logDataAudit(
                source = "mockDownloadDay",
                date = gregorianDate,
                tf = timeframe,
                before = before,
                after = candles
            )
            total += candles.size
        }
        return JsonObject().apply {
            addProperty("saved", "M-day-$gregorianDate-*m.json")
            addProperty("count", total)
            addProperty("timeframes", tfs.joinToString(","))
        }
    }

    suspend fun ensureMockDayTimeframes(gregorianDate: String, missingTfs: List<String>): JsonObject {
        if (missingTfs.isEmpty()) {
            return JsonObject().apply {
                addProperty("ok", true)
                addProperty("downloaded", 0)
            }
        }
        val (from, to) = LocalBotRuntime.gregorianDayRange(gregorianDate)
        var downloaded = 0
        missingTfs.forEach { timeframe ->
            val file = File(mockDir, "M-day-$gregorianDate-${timeframe}m.json")
            val before = readCandlesIfExists(file)
            val candles = HivaRoomClient.getBars(timeframe, from, to)
            file.writeText(candlesToJson(candles))
            logDataAudit(
                source = "ensureMockDayTimeframes",
                date = gregorianDate,
                tf = timeframe,
                before = before,
                after = candles
            )
            downloaded++
        }
        return JsonObject().apply {
            addProperty("ok", true)
            addProperty("downloaded", downloaded)
            addProperty("timeframes", missingTfs.joinToString(","))
        }
    }

    suspend fun mockDownloadMonth(startDate: String, endDate: String, tf: String = "1"): JsonObject {
        val start = LocalDate.parse(startDate)
        val end = LocalDate.parse(endDate)
        var cursor = start
        var total = 0
        while (!cursor.isAfter(end.minusDays(1))) {
            val dateStr = cursor.format(DateTimeFormatter.ISO_DATE)
            val res = mockDownloadDay(dateStr, tf)
            total += res.get("count")?.asInt ?: 0
            cursor = cursor.plusDays(1)
        }
        return JsonObject().apply {
            addProperty("saved", "month-range")
            addProperty("count", total)
        }
    }

    fun cacheList(): JsonArray {
        val out = JsonArray()
        cacheDir.listFiles()?.sortedByDescending { it.lastModified() }?.forEach { f ->
            val parts = f.name.removeSuffix(".json").split("_")
            if (parts.size >= 3) {
                val tf = parts[1]
                val date = parts[2]
                val count = try {
                    JsonParser.parseString(f.readText()).asJsonArray.size()
                } catch (_: Exception) {
                    0
                }
                out.add(JsonObject().apply {
                    addProperty("tf", tf)
                    addProperty("date", date)
                    addProperty("bar_count", count)
                    addProperty("size_kb", (f.length() / 1024.0))
                    addProperty("filename", f.name)
                })
            }
        }
        return out
    }

    fun cacheDelete(tf: String, date: String): JsonObject {
        val f = File(cacheDir, "candles_${tf}_${date}.json")
        val ok = f.exists() && f.delete()
        return JsonObject().apply {
            addProperty("ok", ok)
            addProperty("deleted", f.name)
        }
    }

    suspend fun cacheDownload(tf: String, date: String): JsonObject {
        val (from, to) = LocalBotRuntime.gregorianDayRange(date)
        val file = File(cacheDir, "candles_${tf}_${date}.json")
        val before = readCandlesIfExists(file)
        val candles = HivaRoomClient.getBars(tf, from, to)
        file.writeText(candlesToJson(candles))
        logDataAudit(
            source = "cacheDownload",
            date = date,
            tf = tf,
            before = before,
            after = candles
        )
        return JsonObject().apply {
            addProperty("ok", true)
            addProperty("date", date)
            addProperty("tf", tf)
            addProperty("bar_count", candles.size)
            addProperty("filename", file.name)
        }
    }

    suspend fun cacheDownloadMonth(tf: String, year: Int, month: Int): JsonObject {
        val ym = YearMonth.of(year, month)
        var downloaded = 0
        for (d in 1..ym.lengthOfMonth()) {
            val date = ym.atDay(d).format(DateTimeFormatter.ISO_DATE)
            val f = File(cacheDir, "candles_${tf}_${date}.json")
            if (f.exists()) continue
            cacheDownload(tf, date)
            downloaded++
        }
        return JsonObject().apply {
            addProperty("ok", true)
            addProperty("year", year)
            addProperty("month", month)
            addProperty("downloaded", downloaded)
            addProperty("already_cached", ym.lengthOfMonth() - downloaded)
            addProperty("errors", 0)
            addProperty("skipped_days", 0)
        }
    }

    private fun candlesToJson(candles: List<LocalCandle>): String {
        val arr = JsonArray()
        candles.forEach {
            arr.add(JsonObject().apply {
                addProperty("time", it.time)
                addProperty("open", it.open)
                addProperty("high", it.high)
                addProperty("low", it.low)
                addProperty("close", it.close)
            })
        }
        return arr.toString()
    }

    private fun readCandlesIfExists(file: File): List<LocalCandle>? {
        if (!file.exists()) return null
        return try {
            val root = JsonParser.parseString(file.readText())
            if (!root.isJsonArray) return null
            val arr = root.asJsonArray
            buildList {
                for (i in 0 until arr.size()) {
                    val o = arr[i].asJsonObject
                    val time = o.get("time")?.asLong ?: continue
                    val open = o.get("open")?.asDouble ?: continue
                    val high = o.get("high")?.asDouble ?: continue
                    val low = o.get("low")?.asDouble ?: continue
                    val close = o.get("close")?.asDouble ?: continue
                    add(LocalCandle(time, open, high, low, close))
                }
            }
        } catch (_: Exception) {
            null
        }
    }

    private fun expectedCountForTf(tf: String): Int? = when (tf) {
        "1" -> 1440
        "5" -> 288
        "15" -> 96
        "30" -> 48
        "60" -> 24
        else -> null
    }

    private data class CandleDigest(
        val count: Int,
        val firstTime: Long?,
        val lastTime: Long?,
        val firstClose: Double?,
        val lastClose: Double?,
        val strictAscendingTime: Boolean,
        val uniqueTimeCount: Int,
        val signature: Long
    )

    private data class ReferenceDigest(
        val count: Int,
        val firstTime: Long,
        val lastTime: Long,
        val firstClose: Double,
        val lastClose: Double,
        val signature: Long
    )

    private val referenceByDate: Map<String, Map<String, ReferenceDigest>> = mapOf(
        // Web reference dataset: SH-day-1405-01-23 (Gregorian equivalent 2026-04-12)
        "2026-04-12" to mapOf(
            "1" to ReferenceDigest(1440, 1775786880, 1776007800, 79549.0, 79388.0, -3107878900440706963L),
            "5" to ReferenceDigest(288, 1775810100, 1776007800, 79385.0, 79388.0, 4820164964497034569L),
            "15" to ReferenceDigest(96, 1775825100, 1776007800, 79758.0, 79388.0, -728215243182669335L),
            "30" to ReferenceDigest(48, 1775835000, 1776007800, 79593.0, 79388.0, 4616609768932816953L),
            "60" to ReferenceDigest(24, 1775844000, 1776006000, 79657.0, 79388.0, -5683134918221444067L),
        )
    )

    private fun digest(candles: List<LocalCandle>?): CandleDigest? {
        if (candles == null) return null
        if (candles.isEmpty()) {
            return CandleDigest(
                count = 0,
                firstTime = null,
                lastTime = null,
                firstClose = null,
                lastClose = null,
                strictAscendingTime = true,
                uniqueTimeCount = 0,
                signature = 0L
            )
        }
        val strictAscending = candles.zipWithNext().all { (a, b) -> b.time > a.time }
        var sig = 1125899906842597L
        candles.forEach {
            sig = 31L * sig + it.time
            sig = 31L * sig + (it.open * 100.0).roundToLong()
            sig = 31L * sig + (it.high * 100.0).roundToLong()
            sig = 31L * sig + (it.low * 100.0).roundToLong()
            sig = 31L * sig + (it.close * 100.0).roundToLong()
        }
        return CandleDigest(
            count = candles.size,
            firstTime = candles.first().time,
            lastTime = candles.last().time,
            firstClose = candles.first().close,
            lastClose = candles.last().close,
            strictAscendingTime = strictAscending,
            uniqueTimeCount = candles.map { it.time }.toSet().size,
            signature = sig
        )
    }

    private fun logDataAudit(
        source: String,
        date: String,
        tf: String,
        before: List<LocalCandle>?,
        after: List<LocalCandle>
    ) {
        val b = digest(before)
        val a = digest(after) ?: return
        val expected = expectedCountForTf(tf)
        val countMatch = expected == null || a.count == expected
        val integrityOk = a.strictAscendingTime && a.uniqueTimeCount == a.count
        val changed = b?.signature != null && b.signature != a.signature
        val status = if (countMatch && integrityOk) "MATCH" else "MISMATCH"
        val ref = referenceByDate[date]?.get(tf)
        val refMatch = if (ref == null) {
            "n/a"
        } else {
            val ok = a.count == ref.count &&
                a.firstTime == ref.firstTime &&
                a.lastTime == ref.lastTime &&
                a.firstClose == ref.firstClose &&
                a.lastClose == ref.lastClose &&
                a.signature == ref.signature
            if (ok) "MATCH" else "MISMATCH"
        }
        Log.i(
            tag,
            "DATA_AUDIT $status source=$source date=$date tf=${tf}m count=${a.count} expected=${expected ?: "-"} first=${a.firstTime}/${a.firstClose} last=${a.lastTime}/${a.lastClose} ascending=${a.strictAscendingTime} unique=${a.uniqueTimeCount}/${a.count} signature=${a.signature} changed=${if (b == null) "new_file" else changed} ref=$refMatch"
        )
        if (ref != null && refMatch == "MISMATCH") {
            Log.w(
                tag,
                "DATA_AUDIT_REF_DIFF date=$date tf=${tf}m actual_sig=${a.signature} ref_sig=${ref.signature} actual_first=${a.firstTime}/${a.firstClose} ref_first=${ref.firstTime}/${ref.firstClose} actual_last=${a.lastTime}/${a.lastClose} ref_last=${ref.lastTime}/${ref.lastClose}"
            )
        }
    }
}
